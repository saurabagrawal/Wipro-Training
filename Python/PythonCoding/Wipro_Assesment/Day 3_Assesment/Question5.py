import math
def square_root(num):
    return math.sqrt(num)
def angle(num):
    return math.sin(num)
def gcd(num1 , num2):
    return gcd(num1 , num2)

num1 = float(input('Enter the number: '))
print(square_root(num1))
numm2 = float(input("Enter the number: "))
print(angle(numm2))
num3 = float(input("Enter the number: "))
num4 = float(input("Enter the number: "))
print(gcd(num3, num4))